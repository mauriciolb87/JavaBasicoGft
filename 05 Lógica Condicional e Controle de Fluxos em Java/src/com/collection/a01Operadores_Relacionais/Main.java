package com.collection.a01Operadores_Relacionais;

public class Main {

    public static void main(String[] args) {

        int i1 = 10;
        int i2 = 20;
        System.out.println(i1 == i2);
        System.out.println(i1 != i2);
        System.out.println(i1 > i2);
        System.out.println(i1 <= i2);

        float f1 = 4.5f;
        float f2 = 3.5f;
        System.out.println(f1 == f2);
        System.out.println(f1 != f2);
        System.out.println(f1 >= f2);
        System.out.println(f1 < f2);

        double d1 = 59.6d;

        char c1 = 'x';
        char c2 = 'y';
        System.out.println(c1 == c2);
        System.out.println(c1 != c2);
        System.out.println(c1 >= c2);
        System.out.println(c1 < c2);

        String s1 = "Fulano";
        String s2 = "Fulano";
        String s3 = "Ciclano";

        boolean b1 = true;
        boolean b2 = false;
        System.out.println(b1 == b2);
        System.out.println(b1 != b2);

        long l1 = 1597L;
        long l2 = 899L;
        System.out.println(l1 == i2);
        System.out.println(l2 >= i1);

        byte y1 = 1;
        short h1 = 25;
        System.out.println(y1 != h1);







    }
}
